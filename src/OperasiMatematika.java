public class OperasiMatematika {
    public static void main(String[] args) {

        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);


        System.out.println(" ");
        System.out.println(" ");
        int c = 100;

        System.out.println(c+=10);
        System.out.println(c-=10);
        System.out.println(c*=10);
        System.out.println(c/=10);
        System.out.println(c%=10);

        int d = 100;
        int e = -10;

        d++;
        System.out.println(d);
        d--;
        System.out.println(d);

        e++;
        System.out.println(e);

        System.out.println(!true);
        System.out.println(!false);

    }
}
